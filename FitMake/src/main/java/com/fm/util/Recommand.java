package com.fm.util;

public class Recommand {

	private int iNo = 0;
	private int bNo = 0;

	public Recommand() {
		super();
	}

	public Recommand(int iNo, int bNo) {
		super();
		this.iNo = iNo;
		this.bNo = bNo;
	}

	public int getiNo() {
		return iNo;
	}

	public void setiNo(int iNo) {
		this.iNo = iNo;
	}

	public int getbNo() {
		return bNo;
	}

	public void setbNo(int bNo) {
		this.bNo = bNo;
	}
	
}
